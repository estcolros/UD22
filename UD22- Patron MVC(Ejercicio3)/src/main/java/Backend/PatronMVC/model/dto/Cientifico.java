package Backend.PatronMVC.model.dto;

public class Cientifico {
	private String dniCientifico;
	private String nomApelsCientifico;
	
	public String getDniCientifico() {
		return dniCientifico;
	}
	public void setDniCientifico(String dniCientifico) {
		this.dniCientifico = dniCientifico;
	}
	public String getNomApelsCientifico() {
		return nomApelsCientifico;
	}
	public void setNomApelsCientifico(String nomApelsCientifico) {
		this.nomApelsCientifico = nomApelsCientifico;
	}
	
	
}
